package br.com.ChronosAcademy.logica_de_programacao;

public class AlturaRetangulo {

    public static void main(String[] args) {
      /*
      Com a base e a altura de um retângulo, calcular e exibir sua área.
      Dica: Area retangulo = base x altura
      */

     int base = 9;
     int altura = 4;
     int retangulo = base * altura;

     String texto = "A área do retângulo é: ";
     System.out.println(texto+retangulo);

    }
}
